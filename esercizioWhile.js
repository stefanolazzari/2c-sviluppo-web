let ripeti = 5;
let i = 0;


//stampa la variabile "ripeti" per il numero di volte pari al suo valore
while (i < ripeti) {
    console.log(ripeti);
    i++;
}


// crea conto alla rovescia #1
while (i < ripeti) {
    console.log(ripeti);
    ripeti--;
}


//conto alla rovescia #2
while (i < ripeti) {
    console.log(ripeti-i);
    i++;
}